
//add
const add = function (x, y) {
    return x + y;
};
//substract
const subtract = function (x, y) {
    return x - y;
};
//multiply
const multiply = function (x, y) {
    return x * y;
};
//divide
const divide = function (x, y) {
    return x / y;
};

//operate
const operate = function (operator, x, y) {
    return operator(x, y)
}